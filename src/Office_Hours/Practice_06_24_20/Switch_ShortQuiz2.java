package Office_Hours.Practice_06_24_20;

public class Switch_ShortQuiz2 {
    public static void main(String[] args) {
        String opt = "true";
        switch (opt){
            case "true":
                System.out.println("True");
            default:
                System.out.println("False");
        }

        System.out.println("Done");
    }

}
