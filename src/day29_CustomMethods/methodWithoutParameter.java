package day29_CustomMethods;
/*
step1: print hello 5 times
step2: print your school name 1 time
step3: print hello 5 times
step4: print your name 1 time
step5: print hello 5 times
 */
public class methodWithoutParameter {

    public static void main(String[] args) {

        for(int i=10; i>=6; i--){            // step1
            System.out.println("Hello");
        }

        System.out.println("Cybertek");     // step2

        for(int i=10; i>=6; i--) {
            System.out.println("Hello");    // step3
        }

        System.out.println("Elvira");       // step4

        for(int i=10; i>=6; i--) {
            System.out.println("Hello");   // step5
        }





    }

}
